interface DailyQuoteState {
  date: string;
  index: number;
}

function todayDateString(now: number): string {
  return new Date(now).toISOString().slice(0, 10);
}

function deterministicIndex(panelId: string, totalQuotes: number, now: number): number {
  const salt = [...panelId].reduce((sum, char, i) => sum + char.charCodeAt(0) * (i + 1), 0);
  const dayBucket = Math.floor(now / (24 * 60 * 60 * 1000));
  return (dayBucket + salt) % totalQuotes;
}

export function getInitialQuoteIndex(panelId: string, totalQuotes: number, now = Date.now()): number {
  if (totalQuotes <= 0) return 0;
  const key = `worldmonitor-quote-daily-${panelId}`;
  const today = todayDateString(now);
  try {
    const raw = localStorage.getItem(key);
    if (raw) {
      const state: DailyQuoteState = JSON.parse(raw) as DailyQuoteState;
      if (state.date === today && typeof state.index === 'number') {
        return state.index % totalQuotes;
      }
    }
  } catch {
    // ignore parse errors
  }
  const index = deterministicIndex(panelId, totalQuotes, now);
  try {
    localStorage.setItem(key, JSON.stringify({ date: today, index }));
  } catch {
    // ignore storage errors
  }
  return index;
}

export function saveDailyQuoteIndex(panelId: string, index: number, now = Date.now()): void {
  const key = `worldmonitor-quote-daily-${panelId}`;
  try {
    localStorage.setItem(key, JSON.stringify({ date: todayDateString(now), index }));
  } catch {
    // ignore storage errors
  }
}
